public class Int2 {
    public int x;
    public int y;

    public Int2(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public Int2() {
        // x and y default to 0
    }

    public String toString() {
        return "(" + x + ", " + y + ")";
    }
}
